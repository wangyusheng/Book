package bean;

public class ClubBean {

	private int club_id;

	private int user_id;

	private String topic;

	private String recommend_book;

	private String time;

	private String address;

	private int enroll_num;
	private int concern_num;

	private int accusation_num;

	private String generate_time;

	public int getClub_id() {
		return club_id;
	}

	public void setClub_id(int club_id) {
		this.club_id = club_id;
	}

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

	public String getTopic() {
		return topic;
	}

	public void setTopic(String topic) {
		this.topic = topic;
	}

	public String getRecommend_book() {
		return recommend_book;
	}

	public void setRecommend_book(String recommend_book) {
		this.recommend_book = recommend_book;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getEnroll_num() {
		return enroll_num;
	}

	public void setEnroll_num(int enroll_num) {
		this.enroll_num = enroll_num;
	}

	public int getConcern_num() {
		return concern_num;
	}

	public void setConcern_num(int concern_num) {
		this.concern_num = concern_num;
	}

	public int getAccusation_num() {
		return accusation_num;
	}

	public void setAccusation_num(int accusation_num) {
		this.accusation_num = accusation_num;
	}

	public String getGenerate_time() {
		return generate_time;
	}

	public void setGenerate_time(String generate_time) {
		this.generate_time = generate_time;
	}

}
